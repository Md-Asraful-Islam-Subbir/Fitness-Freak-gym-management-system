    <h1>Gym Diet Tips</h1>

    <h2>General Guidelines:</h2>
    <ul>
        <li>Consume a balanced diet with a mix of proteins, carbohydrates, and healthy fats.</li>
        <li>Eat multiple small meals throughout the day to keep your metabolism active.</li>
        <li>Stay hydrated by drinking plenty of water throughout the day.</li>
        <li>Avoid sugary drinks and opt for water or unsweetened beverages.</li>
    </ul>

    <h2>Pre-Workout Nutrition:</h2>
    <p>Before a workout, consume a light meal that includes easily digestible carbohydrates and a moderate amount of protein.</p>

    <h2>Post-Workout Nutrition:</h2>
    <p>After a workout, refuel with a combination of protein and carbohydrates to aid in recovery and muscle repair.</p>

    <h2>Snack Ideas:</h2>
    <ul>
        <li>Greek yogurt with berries and a drizzle of honey.</li>
        <li>Almonds or mixed nuts for a quick protein boost.</li>
        <li>Sliced veggies with hummus.</li>
    </ul>

    <h2>Important Note:</h2>
    <p>Remember that individual nutritional needs can vary. For personalized diet plans, consult a certified nutritionist or dietitian.</p>

